#include "libft.h"

int	ft_int_length(int n)
{
	int len;

	len = 0;
	if (n <= 0)
		len++;
	while (n != 0)
	{
		if (n != 0)
		{
			n = n / 10;
			len++;
		}
	}
	return (len);
}
