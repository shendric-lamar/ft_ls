#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *cs;

	cs = (char *)s;
	while ((*cs != (char)c) && (*cs != '\0'))
		cs++;
	if ((*cs == '\0') && (c != '\0'))
		return (NULL);
	return (cs);
}
